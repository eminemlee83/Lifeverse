/** 문자열 해시 — 항상 0 이상의 32비트 정수를 반환한다 (부호 있는 시프트로 인한
 *  음수 버그가 v9~v11에서 실제로 한 번 났었으므로, >>> 0 을 반드시 유지). */
function hashStr(s) {
  let h = 0;
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0;
  return h;
}

/** 받침 유무에 따라 을/를을 올바르게 고른다 (오프라인 폴백 문장이 어색하지 않도록).
 *  v14에서 이 함수를 안 쓰고 하드코딩("을(를)")해서 조사 오류가 났던 적이 있다 —
 *  세계관 문구를 다룰 때는 반드시 이 함수를 거칠 것. */
function withObjectParticle(word) {
  const last = word.charCodeAt(word.length - 1);
  if (last < 0xac00 || last > 0xd7a3) return word + '를'; // 한글 음절 범위 밖이면 기본값
  const hasBatchim = (last - 0xac00) % 28 !== 0;
  return word + (hasBatchim ? '을' : '를');
}

/** 받침 유무에 따라 이/가를 올바르게 고른다 (v2 신규 — 모티프 teaser가
 *  명사구라서 주격 조사가 필요해졌다). withObjectParticle과 같은 원칙:
 *  모티프 문구를 문장에 끼울 때는 반드시 이 함수를 거칠 것. */
function withSubjectParticle(word) {
  const last = word.charCodeAt(word.length - 1);
  if (last < 0xac00 || last > 0xd7a3) return word + '가';
  const hasBatchim = (last - 0xac00) % 28 !== 0;
  return word + (hasBatchim ? '이' : '가');
}

/**
 * 카테고리 id로 세계관을 조회한다. 없는 카테고리(또는 구버전 저장 데이터로
 * categoryId가 비어있는 경우)는 freegoal로 안전하게 폴백한다.
 */
function getVocab(categoryId) {
  return WORLD_VOCAB[categoryId] || WORLD_VOCAB.freegoal;
}

/** 카테고리 id로 모티프를 조회한다. getVocab과 동일한 폴백 정책. (v2 신규) */
function getMotifs(categoryId) {
  return WORLD_MOTIFS[categoryId] || WORLD_MOTIFS.freegoal;
}

/**
 * 진행 일수(dayNo)에 따라 여정의 몇 단계에 있는지 계산한다.
 * 대략 1주일 단위로 다음 단계로 넘어가고, 마지막 단계 도달 후에는 고정된다.
 * (v14에서 1일째/8일째/15일째 세 지점으로 전환을 실제 검증함)
 */
function getStageIndex(vocab, dayNo) {
  return Math.min(vocab.stages.length - 1, Math.floor((dayNo - 1) / 7));
}

function getStage(categoryId, dayNo) {
  const vocab = getVocab(categoryId);
  return vocab.stages[getStageIndex(vocab, dayNo)];
}

